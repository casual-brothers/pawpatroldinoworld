void UK2Node_FSMState::EarlyValidation(class FCompilerResultsLog& MessageLog) const
{
	Super::EarlyValidation(MessageLog);

	// Checks if FSMStateClass has not been assigned 
	const UEdGraphPin* FSMStatePin = FindPin(FSMStateClassPinName, EGPD_Input);
	for (UEdGraphPin* Pin : Pins)
	{
		if (Pin->LinkedTo.Num() > 0 && FSMStatePin && !FSMStatePin->DefaultObject)
		{
			MessageLog.Error(*LOCTEXT("MissingFSMState", "Missing FSM State @@").ToString(), this);
			break;
		}
	}
}

