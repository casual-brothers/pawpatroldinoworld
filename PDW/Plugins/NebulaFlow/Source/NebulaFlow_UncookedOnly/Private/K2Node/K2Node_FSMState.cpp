void UK2Node_FSMState::AllocateDefaultPins()
{
	Super::AllocateDefaultPins();
	ValidateLabel();
	// Creates input pin
	CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Exec, UEdGraphSchema_K2::PN_Execute);

	// Creates FSMStateLabelPin
	if (!FindPin(FSMStateLabelPinName, EGPD_Input))
	{
		UEdGraphPin* FSMStateLabelPin = CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Name, FSMStateLabelPinName);
		if (FSMStateLabelPin)
		{
			GetDefault<UEdGraphSchema_K2>()->SetPinAutogeneratedDefaultValue(FSMStateLabelPin, Label.ToString());
			FSMStateLabelPin->bNotConnectable = true;
		}
	}
	// Creates FSMStateParameterPin
	if (!FindPin(FSMStateParameterPinName, EGPD_Input))
	{
		UEdGraphPin* FSMStateParameter = CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Name, FSMStateParameterPinName);
		if (FSMStateParameter)
		{
			GetDefault<UEdGraphSchema_K2>()->SetPinAutogeneratedDefaultValue(FSMStateParameter, Parameter.ToString());
			FSMStateParameter->bNotConnectable = true;
		}
	}

	// Creates FSMStateClassPin
	if (!FindPin(FSMStateClassPinName, EGPD_Input))
	{
		UEdGraphPin* FSMStateClassPin = CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Class, UNebulaFlowBaseFSMState::StaticClass(), FSMStateClassPinName);
		if (FSMStateClassPin)
		{
			FSMStateClassPin->bNotConnectable = true;
		}
	}

	PinNames.Empty();
	for (FGameplayTag PinTag : PinTagsContainer)
	{
		PinNames.Add(PinTag.GetTagName());
	}
	for (FName& PinName : PinNames)
	{
		// Label with Name_NONE is not allowed
		if (PinName == NAME_None)
		{
			PinName = UNebulaFlowK2NodeFunctionLibrary::GetUniqueName(PinNames, DefaultOutputPinName);
		}

		// Creates output pins
		if (!FindPin(PinName, EGPD_Output))
		{
			CreatePin(EGPD_Output, UEdGraphSchema_K2::PC_Exec, PinName);
		}
	}

	UNebulaFlowBaseFSM* FSMOwner = UNebulaFlowK2NodeFunctionLibrary::GetFSMOwner(this);
	if (FSMOwner)
	{
		FFSMStateDescriptor* StateDataDescriptor = FSMOwner->FSMStructureMap.Find(Label);
		if (StateDataDescriptor)
		{
			// Sort TransitionToStateMap
			UNebulaFlowK2NodeFunctionLibrary::SortFSMStructureMapAsOutputPinList(PinNames, StateDataDescriptor->TransitionToStateMap);
		}
	}
}

void UK2Node_FSMState::ReconstructNode()
{
	// Updates label and parameters with node's value
	UEdGraphPin* FSMStateLabelPin = FindPin(FSMStateLabelPinName, EGPD_Input);
	if (FSMStateLabelPin)
	{
		Label = FName(*FSMStateLabelPin->DefaultValue);
		LabelTag = FGameplayTag::RequestGameplayTag(FName(*FSMStateLabelPin->DefaultValue), false);
	}
	UEdGraphPin* FSMStateParameterPin = FindPin(FSMStateParameterPinName, EGPD_Input);
	if (FSMStateLabelPin)
	{
		Parameter = FName(*FSMStateParameterPin->DefaultValue);
	}

	Super::ReconstructNode();
}

void UK2Node_FSMState::PostPasteNode()
{
	Super::PostPasteNode();

	// Checks duplicate labels
	UEdGraphPin* FSMStateLabelPin = FindPin(FSMStateLabelPinName, EGPD_Input);
	if (FSMStateLabelPin)
	{
		TArray<FName> StateLabelArray = {};
		GetStateLabelsInGraph(StateLabelArray);
		if (StateLabelArray.Contains(FName(*FSMStateLabelPin->DefaultValue)))
		{
			UE_LOG(LogNebulaFlowK2Node, Log, TEXT("State with label %s already exist."), *FSMStateLabelPin->DefaultValue);
			const FName UniqueName = UNebulaFlowK2NodeFunctionLibrary::GetUniqueName(StateLabelArray, "Copied");
			FSMStateLabelPin->DefaultValue = UniqueName.ToString();
		}
	}
}

void UK2Node_FSMState::DestroyNode()
{
	// Removes label state from FSMStructureMap
	UNebulaFlowBaseFSM* FSMOwner = UNebulaFlowK2NodeFunctionLibrary::GetFSMOwner(this);
	if (FSMOwner && FSMOwner->FSMStructureMap.Contains(Label))
	{
		FSMOwner->FSMStructureMap.Remove(Label);
		FSMOwner->FSMStructureMap.Compact();
		FSMOwner->FSMStructureMap.Shrink();
	}

	Super::DestroyNode();
}

