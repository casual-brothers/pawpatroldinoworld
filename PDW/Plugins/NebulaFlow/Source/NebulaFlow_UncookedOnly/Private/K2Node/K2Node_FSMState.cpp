/* Begin UObject interface */

void UK2Node_FSMState::PostEditChangeProperty(struct FPropertyChangedEvent& PropertyChangedEvent)
{
	Super::PostEditChangeProperty(PropertyChangedEvent);

	FName PropertyName = (PropertyChangedEvent.Property != nullptr) ? PropertyChangedEvent.Property->GetFName() : NAME_None;
	if (PropertyName == TEXT("PinNames"))
	{
		// Duplicates are not allowed
		const int32 AddedElementIndex = PropertyChangedEvent.GetArrayIndex(PropertyName.ToString());
		if (AddedElementIndex >= 0 && AddedElementIndex < PinNames.Num())
		{
			FString PinString = PinNames[AddedElementIndex].ToString();
			PinString.ReplaceInline(TEXT(" "), TEXT(""));
			PinNames[AddedElementIndex] = FName(*PinString);

			const int32 FoundedIndex = PinNames.Find(PinNames[AddedElementIndex]);
			const int32 FoundedLastIndex = PinNames.FindLast(PinNames[AddedElementIndex]);
			if (FoundedIndex != AddedElementIndex || FoundedLastIndex != AddedElementIndex)
			{
				UE_LOG(LogNebulaFlowK2Node, Log, TEXT("Pin %s already exist."), *(PinNames[AddedElementIndex].ToString()));
				PinNames[AddedElementIndex] = UNebulaFlowK2NodeFunctionLibrary::GetUniqueName(PinNames, DefaultOutputPinName);
			}
		}

		ReconstructNode();
		GetGraph()->NotifyGraphChanged();
	}
	else if (PropertyName == TEXT("LabelTag"))
	{
		UEdGraphPin* FSMStateLabelPin = FindPin(FSMStateLabelPinName, EGPD_Input);
		if (FSMStateLabelPin)
		{
			FSMStateLabelPin->DefaultValue = LabelTag.ToString();
			Label = LabelTag.GetTagName();
		}
	}
	else if (PropertyName == TEXT("PinTagsContainer"))
	{
		const int32 AddedElementIndex = PropertyChangedEvent.GetArrayIndex(PropertyName.ToString());
		if (AddedElementIndex >= 0 && AddedElementIndex < PinTagsContainer.Num())
		{
			FString PinString = PinTagsContainer[AddedElementIndex].ToString();
			PinString.ReplaceInline(TEXT(" "), TEXT(""));
			PinTagsContainer[AddedElementIndex] = FGameplayTag::RequestGameplayTag(FName(PinString), false);

			const int32 FoundedIndex = PinTagsContainer.Find(PinTagsContainer[AddedElementIndex]);
			const int32 FoundedLastIndex = PinTagsContainer.FindLast(PinTagsContainer[AddedElementIndex]);
			if (FoundedIndex != AddedElementIndex || FoundedLastIndex != AddedElementIndex)
			{
				UE_LOG(LogNebulaFlowK2Node, Log, TEXT("Pin %s already exist."), *(PinTagsContainer[AddedElementIndex].ToString()));
				PinTagsContainer[AddedElementIndex] = FGameplayTag::RequestGameplayTag(FSMDefaultPinTag, false);
			}
		}

		PinNames.Empty();
		for (FGameplayTag PinTag : PinTagsContainer)
		{
			PinNames.Add(PinTag.GetTagName());
		}

		ReconstructNode();
	}
}

void UK2Node_FSMState::PostEditUndo()
{
	Super::PostEditUndo();

	for (UEdGraphPin* Pin : Pins)
	{
		UpdateTransitions(Pin);
		UpdateInputPinsValue(Pin);
	}
}

